package com.condition;
class IfElseIfControlStructure
{
	public static void main(String args[])
	{
		int colorValue = 3;
		if(colorValue == 1)
		{
			System.out.println("color Blue !!!");
		}
		else if(colorValue == 2)
		{
			System.out.println("color Red !!!");
		}
		else
		{
			System.out.println("color Green !!!");
		}
	}
}
